#!/usr/bin/env python3
"""
Compile-time string encryption for SPECTER implant.

Scans C source files for ENCRYPTED_STRING("...") macros and generates
a header file with encrypted byte arrays using a random per-build XOR key.

Usage:
    python encrypt_strings.py <src_dir> <output_header>

The output header defines:
    - A random XOR key (first byte of each blob)
    - Encrypted byte arrays for each string
    - ENCRYPTED_STRING macro that references the generated arrays

Decryption at runtime via spec_decrypt_string():
    key = blob[0], plaintext[i] = blob[i+1] ^ key
"""

import os
import re
import secrets
import sys


MACRO_PATTERN = re.compile(r'ENCRYPTED_STRING\(\s*"([^"]*?)"\s*\)')


def encrypt_string(plaintext: str, xor_key: int) -> list[int]:
    """Encrypt a string: [xor_key, encrypted_byte_0, encrypted_byte_1, ...]"""
    encoded = plaintext.encode("ascii")
    blob = [xor_key]
    for b in encoded:
        blob.append(b ^ xor_key)
    return blob


def scan_sources(src_dir: str) -> dict[str, str]:
    """Scan .c and .h files for ENCRYPTED_STRING macros. Returns {string: var_name}."""
    strings = {}
    counter = 0

    for root, _dirs, files in os.walk(src_dir):
        for fname in sorted(files):
            if not (fname.endswith(".c") or fname.endswith(".h")):
                continue
            fpath = os.path.join(root, fname)
            with open(fpath, "r", encoding="utf-8", errors="ignore") as f:
                content = f.read()
            for match in MACRO_PATTERN.finditer(content):
                s = match.group(1)
                if s not in strings:
                    var_name = f"_enc_str_{counter}"
                    strings[s] = var_name
                    counter += 1

    return strings


def generate_header(strings: dict[str, str], xor_key: int) -> str:
    """Generate the encrypted strings header file."""
    lines = [
        "/**",
        " * SPECTER Implant — Encrypted Strings (auto-generated)",
        " *",
        " * Generated by encrypt_strings.py. Do not edit manually.",
        f" * XOR key: 0x{xor_key:02X}",
        " */",
        "",
        "#ifndef ENCRYPTED_STRINGS_H",
        "#define ENCRYPTED_STRINGS_H",
        "",
        '#include "specter.h"',
        "",
    ]

    for plaintext, var_name in sorted(strings.items(), key=lambda x: x[1]):
        blob = encrypt_string(plaintext, xor_key)
        byte_str = ", ".join(f"0x{b:02X}" for b in blob)
        lines.append(f"/* \"{plaintext}\" ({len(plaintext)} chars) */")
        lines.append(
            f"static const BYTE {var_name}[{len(blob)}] = {{ {byte_str} }};"
        )
        lines.append(
            f"#define {var_name.upper()}_LEN {len(blob)}"
        )
        lines.append("")

    # Generate a lookup macro
    lines.append("/*")
    lines.append(" * Usage: ENCRYPTED_STRING(\"literal\") expands to the variable name.")
    lines.append(" * At runtime, call spec_decrypt_string(var, var_LEN, output_buf).")
    lines.append(" */")
    lines.append("")
    lines.append("#endif /* ENCRYPTED_STRINGS_H */")
    lines.append("")

    return "\n".join(lines)


def main():
    if len(sys.argv) < 3:
        print("Usage: encrypt_strings.py <src_dir> <output_header>")
        print("  src_dir       — directory to scan for ENCRYPTED_STRING macros")
        print("  output_header — path to write the generated header")
        sys.exit(1)

    src_dir = sys.argv[1]
    output_path = sys.argv[2]

    if not os.path.isdir(src_dir):
        print(f"[!] Source directory not found: {src_dir}")
        sys.exit(1)

    strings = scan_sources(src_dir)

    if not strings:
        print("[*] No ENCRYPTED_STRING macros found. Generating empty header.")

    xor_key = secrets.randbelow(255) + 1  # 1-255, never 0
    header = generate_header(strings, xor_key)

    os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(header)

    print(f"[+] Generated encrypted strings header: {output_path}")
    print(f"    Strings encrypted: {len(strings)}")
    print(f"    XOR key: 0x{xor_key:02X}")


if __name__ == "__main__":
    main()
